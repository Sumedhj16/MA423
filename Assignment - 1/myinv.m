function B = myinv(A)
	[L, U, P] = gepp(A); % 2n^3/3 + ..

	% PA = LU => U^{-1}L^{-1}P = A^{-1}
    % So A^{-1} = U^{-1}(L^{-1}P)

	X = inv_lower(L, P); % n^3 + .., X = L^{-1}P
	B = inv_upper(U, X); % n^3 + .., B = U^{-1}X

	% Total flop count = 8n^3/3 + O(n^2) flops
end

